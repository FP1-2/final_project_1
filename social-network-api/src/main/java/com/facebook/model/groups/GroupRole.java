package com.facebook.model.groups;

/**
 * Перелік ролей членів групи.
 * Визначає можливі ролі, які можуть бути присвоєні членам групи.
 */
public enum GroupRole {
    /**
     * Адміністратор групи.
     */
    ADMIN,

    /**
     * Звичайний член групи.
     */
    MEMBER,

    /**
     * Заблокований член групи.
     */
    BANNED
}
