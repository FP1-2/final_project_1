package com.facebook.dto.post;

/**
 * Відповідь на дію користувача, пов'язану з постом (лайк або репост).
 *
 * <p>Цей запис використовується для відображення результату взаємодії користувача з постом,
 * такої як додавання або видалення лайка або репосту. Він містить інформацію про те, чи була
 * додана дія (така як лайк або репост) або ж видалена, а також повідомлення, що відображає
 * статус взаємодії.</p>
 *
 * @param added   Прапорець, що вказує, чи була дія додана (true) або видалена (false).
 * @param message Повідомлення, що описує результат взаємодії (наприклад, "Action added" або "Action removed").
 */
public record ActionResponse(boolean added, String message) {}
